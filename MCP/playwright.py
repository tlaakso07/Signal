# Playwright MCP Server
# Installed via: claude mcp add --transport stdio playwright npx @executeautomation/playwright-mcp-server
# Replaces: CLI/playwright-cli/ (retired)
#
# Native MCP browser automation tool. Claude calls browser actions directly as tools —
# no bash commands needed. More reliable and token-efficient than the CLI approach.
#
# Capabilities:
#   - Screenshot any URL (before/after proof for clients)
#   - Navigate age-gated cannabis sites
#   - Execute JavaScript in live browsers
#   - Web scraping
#   - 143 real device profiles for mobile/tablet/desktop testing
#
# Device emulation examples:
#   - iPhone 13, Galaxy S22, iPad Pro — test B&B site on mobile (cannabis customers are mobile-first)
#
# Signal use cases:
#   - QA borednbuzzed.com across breakpoints
#   - Verify contact page fix is live
#   - Test menu embeds and blog posts on mobile
#   - Screenshot client deliverables as proof
#   - Navigate Leafly/POSaBit embeds in a real browser
